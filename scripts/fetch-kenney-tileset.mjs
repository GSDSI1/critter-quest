#!/usr/bin/env node
/**
 * Download Kenney Tiny Town (CC0), extract top-down 16×16 tiles from tilemap_packed.png
 * into public/assets/tiles/kenney/, then merge via import-kenney-tileset.mjs.
 */
import { readFileSync, writeFileSync, mkdirSync, existsSync, readdirSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';
import { spawnSync } from 'child_process';
import { readPng, writePng } from './png-utils.mjs';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const kenneyDir = join(root, 'public/assets/tiles/kenney');
const zipPath = join(root, 'tmp/kenney_tiny-town.zip');
const DL_URL = 'https://opengameart.org/sites/default/files/kenney_tiny-town.zip';

const W = 16;
const STRIDE = 17; // 16px tile + 1px spacing in Kenney packed sheets
const COLS = 12;

/** Kenney tilemap_packed grid → our tileset frame names (see tiles/README.md). */
const EXTRACT = {
  grass: { col: 1, row: 0, frame: 0 },
  path: { col: 3, row: 0, frame: 1 },
  tall_grass: { col: 2, row: 0, frame: 2 },
  water: { col: 0, row: 0, frame: 3 },
  tree: { col: 2, row: 2, frame: 4 },
  wall: { col: 0, row: 5, frame: 5 },
  floor: { col: 1, row: 5, frame: 6 },
  door: { col: 3, row: 4, frame: 7 },
  roof: { col: 0, row: 4, frame: 8 },
  sand: { col: 4, row: 0, frame: 15 },
  water_anim2: { col: 0, row: 1, frame: 20 },
};

function downloadZip() {
  mkdirSync(join(root, 'tmp'), { recursive: true });
  if (existsSync(zipPath) && readFileSync(zipPath).length > 10_000) {
    console.log('Using cached Kenney zip.');
    return;
  }
  console.log('Downloading Kenney Tiny Town (CC0)...');
  const r = spawnSync('curl', ['-sL', '-o', zipPath, DL_URL], { stdio: 'inherit' });
  if (r.status !== 0) throw new Error('curl download failed');
}

function extractPackedSheet() {
  const outDir = join(root, 'tmp/kenney-unzip');
  spawnSync('unzip', ['-q', '-o', zipPath, 'Tilemap/tilemap_packed.png', '-d', outDir], { stdio: 'inherit' });
  const sheetPath = join(outDir, 'Tilemap/tilemap_packed.png');
  if (!existsSync(sheetPath)) throw new Error('tilemap_packed.png missing from zip');
  return readPng(sheetPath);
}

function copyTile(sheet, col, row) {
  const tile = Buffer.alloc(W * W * 4, 0);
  const sx = col * STRIDE;
  const sy = row * STRIDE;
  for (let y = 0; y < W; y++) {
    for (let x = 0; x < W; x++) {
      const si = ((sy + y) * sheet.width + (sx + x)) * 4;
      const di = (y * W + x) * 4;
      tile[di] = sheet.rgba[si];
      tile[di + 1] = sheet.rgba[si + 1];
      tile[di + 2] = sheet.rgba[si + 2];
      tile[di + 3] = sheet.rgba[si + 3];
    }
  }
  return tile;
}

function main() {
  downloadZip();
  const sheet = extractPackedSheet();
  mkdirSync(kenneyDir, { recursive: true });

  const mapping = {
    _comment: 'Auto-generated by fetch-kenney-tileset.mjs — Kenney Tiny Town (CC0).',
  };

  for (const [name, { col, row, frame }] of Object.entries(EXTRACT)) {
    if (col >= COLS) continue;
    const rgba = copyTile(sheet, col, row);
    const filename = `${name}.png`;
    writePng(join(kenneyDir, filename), W, W, rgba);
    mapping[filename] = frame;
    console.log(`  ${filename} ← tilemap (${col},${row}) → frame ${frame}`);
  }

  writeFileSync(
    join(root, 'public/assets/tiles/kenney-map.json'),
    `${JSON.stringify(mapping, null, 2)}\n`,
  );

  console.log('Running import-tileset...');
  const imp = spawnSync('node', ['scripts/import-kenney-tileset.mjs'], { cwd: root, stdio: 'inherit' });
  if (imp.status !== 0) process.exit(imp.status ?? 1);
  console.log('Kenney tiles integrated. Credit: Kenney.nl (CC0). See CREDITS.md.');
}

main();
